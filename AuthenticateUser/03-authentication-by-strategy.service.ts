declare type UsersService = any;
declare const Credentials: any;
const PassportStrategy: Function = () => {};
declare class UnauthorizedException {}

export class AuthByCredentialsService {
  constructor(private usersService: UsersService) {}

  async validateUser(username: string, pass: string): Promise<any> {
    const user = await this.usersService.findOne(username);
    if (user && user.password === pass) {
      const { password, ...result } = user;
      return result;
    }
    return null;
  }
}

export class LocalStrategy extends PassportStrategy(Credentials) {
  constructor(private authService: AuthByCredentialsService) {
    super();
  }

  async validate(username: string, password: string): Promise<any> {
    const user = await this.authService.validateUser(username, password);
    if (!user) {
      throw new UnauthorizedException();
    }
    return user;
  }
}

